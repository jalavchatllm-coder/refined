-- Ограничиваем чтение данных пользователей
alter table auth.users enable row level security;

drop policy if exists "Users can view own auth data" on auth.users;
create policy "Users can view own auth data"
  on auth.users for select
  using (auth.uid() = id);

-- ============================================================================
-- 5. ОЧИСТКА ДАННЫХ (ОПЦИОНАЛЬНО - ДЛЯ ТЕСТИРОВАНИЯ)
-- ============================================================================

-- Функция для очистки старых данных (требует service_role)
create or replace function public.cleanup_old_evaluations(days_to_keep int default 30)
returns void as $$
begin
  delete from public.evaluations
  where created_at < now() - (days_to_keep || ' days')::interval;
end;
$$ language plpgsql security definer;

