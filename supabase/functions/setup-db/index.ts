Deno.serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders });
  }

  try {
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') || Deno.env.get('SUPABASE_ANON_KEY')!;

    const supabase = createClient(supabaseUrl, supabaseServiceKey);

    // Создаём таблицу через SQL
    const { error: createError } = await supabase.rpc('exec_sql', {
      sql: `
        CREATE TABLE IF NOT EXISTS reference_essays (
          id SERIAL PRIMARY KEY,
          text TEXT NOT NULL,
          score INTEGER NOT NULL,
          topics TEXT[] DEFAULT '{}',
          word_count INTEGER,
          created_at TIMESTAMPTZ DEFAULT NOW()
        );
        ALTER TABLE reference_essays ENABLE ROW LEVEL SECURITY;
        DROP POLICY IF EXISTS "Public read" ON reference_essays;
        CREATE POLICY "Public read" ON reference_essays FOR SELECT USING (true);
      `
    });

    // Если RPC не работает — пробуем напрямую вставить данные
    // (таблица уже может существовать)
    const essays = REFERENCE_ESSAYS.map(e => ({
      text: e.text,
      score: e.score,
      topics: e.topics,
      word_count: e.text.trim().split(/\s+/).length
    }));

    // Очищаем и вставляем
    await supabase.from('reference_essays').delete().neq('id', 0);
    const { data, error } = await supabase.from('reference_essays').insert(essays).select('id');

    if (error) {
      return new Response(JSON.stringify({
        status: 'error',
        message: error.message,
        hint: 'Создайте таблицу reference_essays вручную в Supabase Dashboard -> SQL Editor'
      }), { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } });
    }

    return new Response(JSON.stringify({
      status: 'ok',
      inserted: data?.length,
      message: `Загружено ${data?.length} эталонных сочинений`
    }), { headers: { ...corsHeaders, 'Content-Type': 'application/json' } });

  } catch (error: any) {
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
